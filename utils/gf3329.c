uint16_t gf3329_add(uint16_t a, uint16_t b) {
    return a + b < MODULUS ? a + b : a + b - MODULUS;
}

uint16_t gf3329_sub(uint16_t a, uint16_t b) {
    return a < b ? a + MODULUS - b : a - b;
}

uint16_t gf3329_mul(uint16_t a, uint16_t b) {
    return (uint16_t) ((uint32_t) a * b % MODULUS);
}

uint16_t gf3329_inv(uint16_t a) {
    // error if a == 0
    if (a == 0) {
        return 0;
    }
    return gf3329_inv_table[a];
}

uint16_t gf3329_pow(uint16_t a, uint16_t b) {
    uint16_t c = 1;
    uint16_t base = a;

    while (b > 0) {
        if (b % 2 == 1) {
            c = gf3329_mul(c, base);
        }
        base = gf3329_mul(base, base);
        b /= 2;
    }
    return c;
}

